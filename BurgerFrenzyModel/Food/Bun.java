package BurgerFrenzyModel.Food;

/**
 * Class Bun, extends the Ingredient class to represent a bun in the BurgerFrenzy game.
 * This class specifies the ingredient type as a Bun with a predefined name.
 */
public class Bun extends Ingredient {
    /**
     * Constructor for the Bun class.
     * Initializes a Bun object with a predefined name set to "Bun".
     */
    public Bun(){
        this.name = "Bun";
    }

}