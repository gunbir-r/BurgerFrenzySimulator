package BurgerFrenzyModel.Food;

/**
* Class Tomato, extends the Ingredient class to represent a Tomato in the BurgerFrenzy game.
 * This class specifies the ingredient type as a Tomato with a predefined name.
 */
public class Tomato extends Ingredient {
    //attributes
    /**
     * Constructor for the Tomato class.
     * Initializes a Tomato object with a predefined name set to "Tomato".
     */
    public Tomato(){
        this.name = "Tomato";
    }

}
