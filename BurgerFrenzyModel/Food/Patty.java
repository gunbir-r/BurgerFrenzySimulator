package BurgerFrenzyModel.Food;

 /**
* Class Patty, extends the Ingredient class to represent a Patty in the BurgerFrenzy game.
 * This class specifies the ingredient type as a Patty with a predefined name.
 */
public class Patty extends Ingredient {
    /**
     * Constructor for the Patty class.
     * Initializes a Patty object with a predefined name set to "Patty".
     */
    public Patty(){
        this.name = "Patty";
    }

}
